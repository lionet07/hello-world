//一个旅行社有 72 人，其中会英语的有 48 人，会俄语的有 36 人，两样都不会的有 8 人，两样都会的有
//_______个。
//编程：输入旅行社有72人
//     输入会英语48人
//     输入会俄语36人
//     输入两样都不会有8人
//     输出两样都会的有   人 
//     输出：输入数据错误，无法计算 

#include<stdio.h>
int main(){
	int x, y, z, m, n;//旅行社人数为x,会英语的人数为y,会俄语的人数为z,两样都不会的人数为m,两样都会的人数为n 
	printf("x= ; y= ; z= ; m= ；\n");
	scanf("%d %d %d %d",&x,&y,&z,&m);
	if(x==72,y==48,z==36,m==8){
		n=y+z+m-x;
		printf("n=%d",n);
	} 
	else{
		printf("输入数据错误，无法计算。");
	}
	return 0;
} 
